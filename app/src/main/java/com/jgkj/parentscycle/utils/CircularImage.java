	public Bitmap createMask() {
		int i = getWidth();
		int j = getHeight();
		Bitmap.Config localConfig = Bitmap.Config.ARGB_8888;
		Bitmap localBitmap = Bitmap.createBitmap(i, j, localConfig);
		Canvas localCanvas = new Canvas(localBitmap);
		Paint localPaint = new Paint(1);
		localPaint.setColor(Color.BLUE);
		float f1 = getWidth();
		float f2 = getHeight();
		RectF localRectF = new RectF(0.0F, 0.0F, f1, f2);
		localCanvas.drawOval(localRectF, localPaint);
		return localBitmap;
	}

	/**
	 * 获取裁剪后的圆形图片
	 * 
	 * @param radius
	 *            半径
	 */
	public static Bitmap getCroppedRoundBitmap(Bitmap fromBmp, int radius) {
		Bitmap scaledSrcBmp;
		int diameter = radius * 2;

		// 为了防止宽高不相等，造成圆形图片变形，因此截取长方形中处于中间位置最大的正方形图片
		int bmpWidth = fromBmp.getWidth();
		int bmpHeight = fromBmp.getHeight();

		int maxBitmapValue = 100;
		if (bmpWidth > bmpHeight) {
			int tempWidth = bmpWidth;
			bmpWidth = maxBitmapValue;
			bmpHeight = (bmpHeight * bmpWidth) / tempWidth;
		} else if (bmpHeight >= bmpWidth) {
			int tempHeight = bmpHeight;
			bmpHeight = maxBitmapValue;
			bmpWidth = (bmpWidth * bmpHeight) / tempHeight;
		}

		Bitmap bmp = Bitmap.createScaledBitmap(fromBmp, bmpWidth,
				bmpHeight, false);

		int squareWidth = 0, squareHeight = 0;
		int x = 0, y = 0;
		Bitmap squareBitmap;
		if (bmpHeight > bmpWidth) {// 高大于宽
			squareWidth = squareHeight = bmpWidth;
			x = 0;
			y = (bmpHeight - bmpWidth) / 2;
			// 截取正方形图片
			squareBitmap = Bitmap.createBitmap(bmp, x, y, squareWidth,
					squareHeight);
		} else if (bmpHeight < bmpWidth) {// 宽大于高
			squareWidth = squareHeight = bmpHeight;
			x = (bmpWidth - bmpHeight) / 2;
			y = 0;
			squareBitmap = Bitmap.createBitmap(bmp, x, y, squareWidth,
					squareHeight);
		} else {
			squareBitmap = bmp;
		}

		if (squareBitmap.getWidth() != diameter
				|| squareBitmap.getHeight() != diameter) {
			scaledSrcBmp = Bitmap.createScaledBitmap(squareBitmap, diameter,
					diameter, true);

		} else {
			scaledSrcBmp = squareBitmap;
		}
		Bitmap output = Bitmap.createBitmap(scaledSrcBmp.getWidth(),
				scaledSrcBmp.getHeight(), Bitmap.Config.ARGB_8888);
		Canvas canvas = new Canvas(output);

		Paint paint = new Paint();
		Rect rect = new Rect(0, 0, scaledSrcBmp.getWidth(),
				scaledSrcBmp.getHeight());

		paint.setAntiAlias(true);
		paint.setFilterBitmap(true);
		paint.setDither(true);
		canvas.drawARGB(0, 0, 0, 0);
		canvas.drawCircle(scaledSrcBmp.getWidth() / 2,
				scaledSrcBmp.getHeight() / 2, scaledSrcBmp.getWidth() / 2,
				paint);
		paint.setXfermode(new PorterDuffXfermode(PorterDuff.Mode.SRC_IN));
		canvas.drawBitmap(scaledSrcBmp, rect, rect, paint);
		// bitmap回收(recycle导致在布局文件XML看不到效果)
//		 bmp.recycle();
//		 squareBitmap.recycle();
//		 scaledSrcBmp.recycle();
//		fromBmp.recycle();
		bmp = null;
		squareBitmap = null;
		scaledSrcBmp = null;
		fromBmp = null;
		return output;
	}

	/**
	 * 转换图片成圆形
	 * 
	 * @param bitmap
	 *            传入Bitmap对象
	 * @return
	 */
	public static Bitmap toRoundBitmap(Bitmap sourceBitmap) {
		int bmpWidth = sourceBitmap.getWidth();
		int bmpHeight = sourceBitmap.getHeight();
		int maxBitmapValue = 100;
		// if (bmpWidth > bmpHeight && bmpWidth > maxBitmapValue) {
		// int tempWidth = bmpWidth;
		// bmpWidth = maxBitmapValue;
		// bmpHeight = (bmpHeight * bmpWidth) / tempWidth;
		// } else if (bmpHeight >= bmpWidth && bmpHeight > maxBitmapValue){
		// int tempHeight = bmpHeight;
		// bmpHeight = maxBitmapValue;
		// bmpWidth = (bmpWidth * bmpHeight) / tempHeight;
		// }

		if (bmpWidth > bmpHeight) {
			int tempWidth = bmpWidth;
			bmpWidth = maxBitmapValue;
			bmpHeight = (bmpHeight * bmpWidth) / tempWidth;
		} else if (bmpHeight >= bmpWidth) {
			int tempHeight = bmpHeight;
			bmpHeight = maxBitmapValue;
			bmpWidth = (bmpWidth * bmpHeight) / tempHeight;
		}

		Bitmap bitmap = Bitmap.createScaledBitmap(sourceBitmap, bmpWidth,
				bmpHeight, false);

		int width = bitmap.getWidth();
		int height = bitmap.getHeight();
		LogUtil.d(TAG,"had get the bitmap size with:" + width + " height:" + height);
		float roundPx;
		float left, top, right, bottom, dst_left, dst_top, dst_right, dst_bottom;
		if (width <= height) {
			roundPx = width / 2;
			left = 0;
			top = 0;
			right = width;
			bottom = width;
			height = width;
			dst_left = 0;
			dst_top = 0;
			dst_right = width;
			dst_bottom = width;
		} else {
			roundPx = height / 2;
			float clip = (width - height) / 2;
			left = clip;
			right = width - clip;
			top = 0;
			bottom = height;
			width = height;
			dst_left = 0;
			dst_top = 0;
			dst_right = height;
			dst_bottom = height;
		}

		Bitmap output = Bitmap.createBitmap(width, height,
				Bitmap.Config.ARGB_8888);
		Canvas canvas = new Canvas(output);

		final int color = 0xff424242;
		final Paint paint = new Paint();
		final Rect src = new Rect((int) left, (int) top, (int) right,
				(int) bottom);
		final Rect dst = new Rect((int) dst_left, (int) dst_top,
				(int) dst_right, (int) dst_bottom);
		final RectF rectF = new RectF(dst);

		paint.setAntiAlias(true);// 设置画笔无锯齿

		canvas.drawARGB(0, 0, 0, 0); // 填充整个Canvas
		paint.setColor(color);

		// 以下有两种方法画圆,drawRounRect和drawCircle
		// canvas.drawRoundRect(rectF, roundPx, roundPx, paint);//
		// 画圆角矩形，第一个参数为图形显示区域，第二个参数和第三个参数分别是水平圆角半径和垂直圆角半径。
		canvas.drawCircle(roundPx, roundPx, roundPx, paint);

		paint.setXfermode(new PorterDuffXfermode(PorterDuff.Mode.SRC_IN));// 设置两张图片相交时的模式,参考http://trylovecatch.iteye.com/blog/1189452
		canvas.drawBitmap(bitmap, src, dst, paint); // 以Mode.SRC_IN模式合并bitmap和已经draw了的Circle
		// sourceBitmap.recycle();
		sourceBitmap = null;
		return output;
	}
